public class Switch {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 2:
                System.out.println("Samosa");
                break;
            case 1:
                System.out.println("Aloo Matar");
                break;
            default:
                System.out.println("Rishabh Shukla");
                break;
        }
    }
}
