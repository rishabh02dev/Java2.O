public class ifelse {
    public static void main(String[] args) {
        int age =22;
        if(age >=18){
            System.out.println("You can drive");
        }
        if(age>=19){
            System.out.println("Larger");
        }
        //we can put multiple if in if-else
        else{
            System.out.println("You can't drive");
        }
    }
}
